public class LL01_NodeCreation {

    //For Singly Linked List
    class Node1 {
        int data;
        Node1 next;

        Node1(int data){
            this.data = data;
        }
    }

    // For Doubly Linked List
    class Node2 {
        int data;
        Node2 prev;
        Node2 next;

        Node2(int data){
            this.data = data;
        }
    }
}